package collections;

import java.util.LinkedList;

public class LinkedListSpecificmethods {

	public static void main(String[] args) {
		LinkedList l=new LinkedList();
		l.add("Tiger");
		l.add("Lion");
		l.add("fox");
		l.addFirst("First");//adds this as first Element
		l.addLast("Last");// adds this as last element
		l.getFirst();
		l.getLast();
		l.removeFirst();
		l.removeLast();
		
		

	}

}
